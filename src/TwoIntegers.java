public class TwoIntegers 
{
	private int bear;
	private int dear;
	
	public TwoIntegers()
	{
		bear = 0;
		dear = 0;
	}
	public TwoIntegers( int bear, int dear)
	{
		this.bear = bear;
		this.dear = dear;
	}
	public int getBear()
	{
		return bear;
	}
	public void setBear( int bear)
	{
		this.bear = bear;
	}
	public int getDear()
	{
		return dear;
	}
	public void setDear( int dear)
	{
		this.dear = dear;
	}
}
